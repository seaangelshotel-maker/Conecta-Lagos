/**
 * Hook or helper utility function to dynamically translate text at runtime.
 */
export function translateText(source: string, targetLang: string): string {
  if (!source) return '';
  const trimmed = source.trim();
  if (translations[trimmed] && translations[trimmed][targetLang as keyof typeof translations[typeof trimmed]]) {
    return translations[trimmed][targetLang as keyof typeof translations[typeof trimmed]];
  }
  return source;
}
